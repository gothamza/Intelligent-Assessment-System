def get_courses_for_grade_subject(grade: str, subject: str) -> list:
    """
    Get list of courses for a specific grade and subject.
    
    Args:
        grade: Grade level (e.g., "7ème année")
        subject: Subject name (e.g., "Mathématiques")
    
    Returns:
        List of courses for that grade and subject, or empty list if not found
    """
    return MOROCCAN_CURRICULUM.get(grade, {}).get(subject, [])


def get_subjects_for_grade(grade: str) -> list:
    """
    Get list of subjects available for a specific grade.
    
    Args:
        grade: Grade level (e.g., "7ème année")
    
    Returns:
        List of subjects for that grade
    """
    return list(MOROCCAN_CURRICULUM.get(grade, {}).keys())

